public class Variables {
    public static void main(String[]args) {
        short my_variable = 10;
        float my_float  =  4.5f;
        double my_double = 11.5;
        char my_char = '1';
        boolean is_true = false;
        long my_long = 1256;
        String anyWord = "hello world";
        System.out.println(my_variable);
        System.out.println(my_float);
        System.out.println(my_double);
        System.out.println(my_char);
        System.out.println(is_true);
        System.out.println(anyWord);
        System.out.println(my_long);
    }
}

